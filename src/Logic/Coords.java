package Logic;

public class Coords {

    /**
     * x coordinate
     */
    private int x;
    /**
     * y coordinate
     */
    private int y;

    public Coords(int x, int y) {

        this.x = x;
        this.y = y;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }

}
